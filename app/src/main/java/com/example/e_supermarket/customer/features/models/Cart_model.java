package com.example.e_supermarket.customer.features.models;

public class Cart_model
{
    int p_img;
    String p_id;
    String p_name;
    String price;

    public int getP_img() {
        return p_img;
    }

    public void setP_img(int p_img) {
        this.p_img = p_img;
    }

    public String getP_id() {
        return p_id;
    }

    public void setP_id(String p_id) {
        this.p_id = p_id;
    }

    public String getP_name() {
        return p_name;
    }

    public void setP_name(String p_name) {
        this.p_name = p_name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
