package com.example.e_supermarket.customer.features.models;

public class Wishlist_model
{
    int wish_pimg;
    String wish_pid;
    String wish_pname;
    String wish_pprice;

    public int getWish_pimg() {
        return wish_pimg;
    }

    public void setWish_pimg(int wish_pimg) {
        this.wish_pimg = wish_pimg;
    }

    public String getWish_pid() {
        return wish_pid;
    }

    public void setWish_pid(String wish_pid) {
        this.wish_pid = wish_pid;
    }

    public String getWish_pname() {
        return wish_pname;
    }

    public void setWish_pname(String wish_pname) {
        this.wish_pname = wish_pname;
    }

    public String getWish_pprice() {
        return wish_pprice;
    }

    public void setWish_pprice(String wish_pprice) {
        this.wish_pprice = wish_pprice;
    }
}
